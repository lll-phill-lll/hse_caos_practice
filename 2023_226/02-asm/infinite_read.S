// Задача: считываем числа, пока не случится конец ввода. Вывести сумму всех введенных чисел
    .global main
main:
    // обнуляем значение в %edx
    // можно было бы сделать и так: mov $0, %edx
    // но xor работает быстрее и у него меньше размер инструкции
    // intel и amd рекомендуют использовать именно xor
    xor     %edx, %edx

beg:
    // считываем очередное число
    call    readi32
    // Если ввод закончился, то будет выставлен флаг CF (верно только для simpleio)
    // Если CF выставлен, то переходим на метку fin
    jc      fin
    // Прибавим считанное число к %edx
    add     %eax, %edx
    // jmp - безусловный переход. Продолжнаем выполнение с метки beg
    jmp     beg

fin:
    // накопленное число лежит в %edx. Переложим его в %eax, чтобы напечатать
    mov     %edx, %eax
    call    writei32
    call    finish
